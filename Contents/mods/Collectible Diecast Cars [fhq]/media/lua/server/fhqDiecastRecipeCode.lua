function Recipe.OnCreate.DiecastBoxOpenTiered(items, result, player)
	local c = ZombRand(20);
	if c < 6 then
		player:getInventory():AddItem(DiecastCars.VanillaUncommon[ZombRand(#DiecastCars.VanillaUncommon)+1]);
	end
	if c == 6 then
		player:getInventory():AddItem(DiecastCars.VanillaRare[ZombRand(#DiecastCars.VanillaRare)+1]);
	end
	if c == 7 then
		player:getInventory():AddItem(DiecastCars.MotoriousRare[ZombRand(#DiecastCars.MotoriousRare)+1]);
	end
	if c > 7 then
		player:getInventory():AddItem(DiecastCars.AllCommon[ZombRand(#DiecastCars.AllCommon)+1]);
	end
end

